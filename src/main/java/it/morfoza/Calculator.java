public class Calculator {
    public static int add(int number1a, int number2a) {

        return number1a+number2a;
    }

    public static int subtract(int number1a, int number2a) {
        return number1a-number2a;
    }

    public static int multiply(int number1a, int number2a) {
        return number1a*number2a;
    }

    public static int divide(int number1a, int number2a) {
        return number1a/number2a;
    }
}
